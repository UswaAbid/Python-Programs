
# Tuple in python is just like list
# It is immutable i.e we cannot change tuple once it is formed
# We cannot perform append, insert, clear operations in tuple
# we can only perform count and index operations in tuple
# () are optional in tuples


marks = (97, 94, 95, 95, 95)
 
print(marks.count(95))
print(marks.index(95))

# Following structure/line of code is by default tuple
names = "sham", "ali", "ria"
